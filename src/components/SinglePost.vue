<template>
  <div class="post">
    <h3>{{ post.title }}</h3>
    <p>{{ post.body }}</p>
    <span v-for="tag in post.tags" :key="tag"> #{{ tag }} </span>
  </div>
</template>

<script>
import { computed } from 'vue';
export default {
  props: ['post'],
  setup({ post }) {
    const snippet = computed(() => {
      return post.body.substring(0, 3) + '...';
    });
    return { snippet };
  },
};
</script>
